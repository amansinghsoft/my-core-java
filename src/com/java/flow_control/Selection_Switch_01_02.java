package com.java.flow_control;

public class Selection_Switch_01_02 {

	public static void main(String[] args) {
		int x=10;
		switch(x) {
		case 1:
			System.out.println("Aman");
			break;
			
		case 5:
			System.out.println("Hiii");
			break;
			
		case 10:
			System.out.println("Hello");
			break;
			
		default:
			System.out.println("Not Valid");
		}

	}

}
