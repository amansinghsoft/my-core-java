package com.java.fundamentals;

public class FundamentalsArray_2D_02 {

	public static void main(String[] args) {
		int[][] x = new int[2][];
		x[0] = new int[2];
		x[1] = new int[3];

		System.out.println(x);
		System.out.println(x[0]);
		System.out.println(x[0][0]);
		System.out.println(x.length);

	}

}
