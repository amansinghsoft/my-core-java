package com.java.operator_assignment;

public class BitwiseOperator_01_07 {

	public static void main(String[] args) {
		System.out.println(true & false);
		System.out.println(true | false);
		System.out.println(true ^ false);
		System.out.println(4 & 5);
		System.out.println(4 | 5);
		System.out.println(4 ^ 5);

	}

}
