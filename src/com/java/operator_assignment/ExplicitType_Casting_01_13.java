package com.java.operator_assignment;

public class ExplicitType_Casting_01_13 {

	public static void main(String[] args) {

		int x = 130;
		byte b = (byte) x;
		System.out.println(b);

		short s = (short) x;
		System.out.println(s);

		double d = 3673.974;
		int a = (int) d;
		System.out.println(a);

	}

}
